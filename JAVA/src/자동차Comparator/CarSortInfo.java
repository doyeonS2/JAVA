public class CarSortInfo {
    public int productYear; // 연식
    public String carName; // 차 이름
    public int price; // 가격

    public CarSortInfo(int productYear, String carName, int price) {
        this.productYear = productYear;
        this.carName = carName;
        this.price = price;
    }
}
